<?php 	
	// Bitwise AND 
	$First = 5; 
	$second = 3; 
	$answer = $First & $second; 
	
	echo("Bitwise & of 5 and 3 is $answer"); 
	
	echo "<br>"; 
	
	// Bitwise OR 
	$answer = $First | $second; 
	echo("Bitwise | of 5 and 3 is $answer"); 
	
	echo "<br>"; 
	
	// Bitwise XOR 
	$answer = $First ^ $second; 
	echo("Bitwise ^ of 5 and 3 is $answer"); 
	
	echo "<br>"; 
	
	// Bitwise NOT 
	$answer = ~$First; 
	echo("Bitwise ~ of 5 is $answer"); 
	
	echo "<br>"; 
	
	// Bitwise Left shift 
	$second = 1; 
	$answer = $First << $second; 
	echo("5 << 1 will be $answer"); 
	
	echo "<br>"; 
	
	// Bitwise Right shift 
	$answer = $First >> $second; 
	echo("5 >> 1 will be $answer"); 
	
	echo "<br>"; 
echo "<br>";
